#!/bin/bash
# Generates caddy/Caddyfile from .env variables
# Called automatically by auto-setup.sh before starting containers

set -e

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(dirname "$(dirname "$SCRIPT_DIR")")"
CADDYFILE="$ROOT_DIR/caddy/Caddyfile"

# Load .env
if [ ! -f "$ROOT_DIR/.env" ]; then
    echo -e "${RED}✗ .env file not found${NC}"
    exit 1
fi
source "$ROOT_DIR/.env"

# Defaults
DOMAIN_JELLYFIN="${DOMAIN_JELLYFIN:-jellyfin.local}"
DOMAIN_TRANSMISSION="${DOMAIN_TRANSMISSION:-transmission.local}"
DOMAIN_WORDPRESS="${DOMAIN_WORDPRESS:-wordpress.local}"
LETSENCRYPT_EMAIL="${LETSENCRYPT_EMAIL:-admin@example.com}"
ENABLE_SSL="${ENABLE_SSL:-false}"

mkdir -p "$ROOT_DIR/caddy"

echo -e "${BLUE}📝 Generating Caddyfile...${NC}"
echo "  • Jellyfin:     $DOMAIN_JELLYFIN"
echo "  • Transmission: $DOMAIN_TRANSMISSION"
echo "  • WordPress:    $DOMAIN_WORDPRESS"
echo "  • SSL:          $ENABLE_SSL"
echo ""

# ── Local / self-signed mode (no public internet) ────────────────────────────
if [ "$ENABLE_SSL" != "true" ]; then
    cat > "$CADDYFILE" <<EOF
# Generated by setup-caddy.sh — do not edit manually
# Local development mode (no HTTPS)
# http:// prefix forces HTTP-only — prevents any 308 redirect to HTTPS
{
	auto_https off
}

http://$DOMAIN_JELLYFIN {
	reverse_proxy jellyfin:8096 {
		flush_interval -1
		header_up X-Real-IP {remote_host}
		header_up X-Forwarded-For {remote_host}
		header_up X-Forwarded-Proto {scheme}
		header_up X-Forwarded-Host {host}
	}
	header {
		X-Content-Type-Options nosniff
		X-Frame-Options SAMEORIGIN
		-Server
	}
	request_body {
		max_size 20MB
	}
}

http://$DOMAIN_TRANSMISSION {
	reverse_proxy transmission:9091 {
		header_up X-Real-IP {remote_host}
		header_up X-Forwarded-For {remote_host}
		header_up X-Forwarded-Proto {scheme}
	}
	header {
		X-Content-Type-Options nosniff
		X-Frame-Options SAMEORIGIN
		-Server
	}
}

# http://$DOMAIN_WORDPRESS {
# 	reverse_proxy wordpress-app:80 {
# 		header_up X-Real-IP {remote_host}
# 		header_up X-Forwarded-For {remote_host}
# 		header_up X-Forwarded-Proto {scheme}
# 	}
# 	request_body {
# 		max_size 64MB
# 	}
# }
EOF

# ── Production mode (Let's Encrypt) ──────────────────────────────────────────
else
    cat > "$CADDYFILE" <<EOF
# Generated by setup-caddy.sh — do not edit manually
# Production mode (automatic HTTPS via Let's Encrypt)
{
	email $LETSENCRYPT_EMAIL
}

$DOMAIN_JELLYFIN {
	reverse_proxy jellyfin:8096 {
		flush_interval -1
		header_up X-Real-IP {remote_host}
		header_up X-Forwarded-For {remote_host}
		header_up X-Forwarded-Proto {scheme}
		header_up X-Forwarded-Host {host}
	}
	header {
		X-Content-Type-Options nosniff
		X-Frame-Options SAMEORIGIN
		X-XSS-Protection "1; mode=block"
		Permissions-Policy "accelerometer=(), ambient-light-sensor=(), battery=(), bluetooth=(), camera=(), clipboard-read=(), display-capture=(), document-domain=(), encrypted-media=(), gamepad=(), geolocation=(), gyroscope=(), hid=(), idle-detection=(), interest-cohort=(), keyboard-map=(), local-fonts=(), magnetometer=(), microphone=(), payment=(), publickey-credentials-get=(), serial=(), sync-xhr=(), usb=(), xr-spatial-tracking=()"
		Content-Security-Policy "default-src https: data: blob: ; img-src 'self' https://* ; style-src 'self' 'unsafe-inline'; script-src 'self' 'unsafe-inline' https://www.gstatic.com https://www.youtube.com blob:; worker-src 'self' blob:; connect-src 'self'; object-src 'none'; font-src 'self'"
		-Server
	}
	request_body {
		max_size 20MB
	}
}

$DOMAIN_TRANSMISSION {
	reverse_proxy transmission:9091 {
		header_up X-Real-IP {remote_host}
		header_up X-Forwarded-For {remote_host}
		header_up X-Forwarded-Proto {scheme}
	}
	header {
		X-Content-Type-Options nosniff
		X-Frame-Options SAMEORIGIN
		-Server
	}
}

# $DOMAIN_WORDPRESS {
# 	reverse_proxy wordpress-app:80 {
# 		header_up X-Real-IP {remote_host}
# 		header_up X-Forwarded-For {remote_host}
# 		header_up X-Forwarded-Proto {scheme}
# 	}
# 	request_body {
# 		max_size 64MB
# 	}
# }
EOF
fi

echo -e "${GREEN}✓ Caddyfile generated at $CADDYFILE${NC}"
